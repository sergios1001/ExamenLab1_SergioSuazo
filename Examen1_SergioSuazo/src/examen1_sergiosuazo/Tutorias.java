    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        String user,contra;
        try {
            user=tf_user.getText();
            contra=pf_contra.getText();
            boolean login=false;
            int puntero=0;
            for (int i = 0; i < alumnos.size(); i++) {
                if (alumnos.get(i).getUser().equals(user)&&alumnos.get(i).getContra().equals(contra))
                {
                    login=true;
                    puntero=i;
                }
            }
            if(alumnos.get(puntero) instanceof Tutor && login)
            {
                TutoresFrame.setVisible(true);
                TutoresFrame.pack();
                for (int i = 0; i < alumnos.size(); i++) {
                    if(alumnos.get(i) instanceof Tutor)
                    {
                        Tutor t= (Tutor)alumnos.get(i);
                        Object[] newrow = {
                            t.getNombre(),
                            t.getCarrera(),
                            t.getEdad(),
                            t.getCuenta(),
                            t.getUser(),
                            t.getContra(),
                            t.getLugar(),
                            t.getGanancias(),
                            t.getNivel(),
                            t.getTutorias()
                        };
                        DefaultTableModel modelo=(DefaultTableModel) jt_tutores.getModel();
                        modelo.addRow(newrow);
                        jt_tutores.setModel(modelo);
                    }
                }
            }
            else if(alumnos.get(puntero) instanceof Normal && login)
            {
                NormalFrame.setVisible(true);
                NormalFrame.pack();
                for (int i = 0; i < alumnos.size(); i++) {
                    if(alumnos.get(i) instanceof Normal)
                    {
                        Normal t= (Normal)alumnos.get(i);
                        Object[] newrow = {
                            t.getNombre(),
                            t.getCarrera(),
                            t.getEdad(),
                            t.getCuenta(),
                            t.getUser(),
                            t.getContra(),
                            t.getLugar(),
                            t.getNivel()
                        };
                        DefaultTableModel modelo=(DefaultTableModel) jt_normal.getModel();
                        modelo.addRow(newrow);
                        jt_normal.setModel(modelo);
                    }
                }
            }
            
            else{
                JOptionPane.showMessageDialog(this, "Usuario o contrasena equivocada");
            }
            tf_user.setText("");
            pf_contra.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Mala ingresion de datos");
        }
    }//GEN-LAST:event_jButton2MouseClicked

